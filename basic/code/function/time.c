/**
 * 
 * requires			void
 *
 * returns			void
 * 
 * synopsis			time := time()
 * 					returns the current value of time, measured as an integer (number of seconds, as per standard Unix definition)
 * 
 */
void _function_time( void )
{

	if ( !expect_token( PUNC_CLOSE_BRACKET ) )
		{ set_error_state( ERROR_EXPECTED_CLOSE_BRACKET );    return; }
	
	// no concept of time yet (will come from kernel eventually) so return 0 for now
	push_operand_integer( 0 );

}
