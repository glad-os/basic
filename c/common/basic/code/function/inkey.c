/**
 * 
 * requires			void
 *
 * returns			void
 * 
 * synopsis			inkey := inkey(x)
 * 					x is an Integer from 0x00 - 0xff inclusive. Returns a Boolean indicating if key is currently pressed
 * 
 */
void _function_inkey( void )
{

	// TODO : this works fine but there's a REAL LAG with the events filtering through the X system. How to do this better?
	
	int type;

	// INKEY requires a single-element parameter list (containing an INTEGER).
	if ( !expect_expression() )
		{ set_error_state( ERROR_SYNTAX ); return; }
	if ( !expect_token( PUNC_CLOSE_BRACKET ) )
		{ set_error_state( ERROR_EXPECTED_CLOSE_BRACKET );    return; }
	
 	// check for a type mismatch
 	type = *(operand_stack_ptr+operand_stack_offset-1);
 	switch ( type )
 	{
 		case DATATYPE_INTEGER : 
 			// currently, there is no kernel concept of key being held down, so just return false for now
 			push_operand_boolean( 0 );
 			// push_operand_boolean( keypressStatus[pop_operand_integer()] == 1 );
 			break;
 		default               : 
 			set_error_state( ERROR_TYPE_MISMATCH );	return; 
 			break; 
 	}
	
}
