/**
 * 
 * requires			void
 *
 * returns			void
 * 
 * synopsis			REM := REM <string>
 * 					string is defined to be the remainder of the current line of source
 * 
 * 					REM is short for "REMark", and allows the BASIC language to offer the facility for inline commentary.
 * 
 */
void _rem( void )
{
	
	// do nothing - ignore rest of line
	struct lex_result result; 
	while ( next_token(&result,TRUE) ) { }
	
}
